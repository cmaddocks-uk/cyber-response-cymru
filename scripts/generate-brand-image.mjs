// One-off generator: renders the navy "brand chip" (disc + CIRP wordmark +
// descriptor on a navy background) from SVG to PNG, then commits the PNG
// bytes as a base64 string in `src/lib/document-model/brand-image.ts`.
//
// Inlining as base64 means the docx export pipeline does not need
// filesystem access at runtime — the same module works in the Node
// examples script and the browser docx exporter.
//
// Re-run with `npm run generate:brand-image` only if the brand chip ever
// changes (e.g. swapping the colour palette or the wordmark).

import { Resvg } from '@resvg/resvg-js';
import { writeFileSync, readFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');
const glyphsPath = path.join(root, 'src', 'components', 'brand-glyphs.ts');
const outPath = path.join(root, 'src', 'lib', 'document-model', 'brand-image.ts');

// Pull the Fraunces 3 and 0 paths out of the existing brand-glyphs module
// rather than duplicating them.
const glyphsSrc = readFileSync(glyphsPath, 'utf8');
const m3 = glyphsSrc.match(/BRAND_3_PATH = "([^"]+)"/);
const m0 = glyphsSrc.match(/BRAND_0_PATH = "([^"]+)"/);
if (!m3 || !m0) throw new Error('Could not extract glyph paths from brand-glyphs.ts');
const PATH_3 = m3[1];
const PATH_0 = m0[1];

// SVG source — 480×120 chip (4:1 ratio), navy background, disc on left,
// Fraunces "CIRP" + IBM Plex descriptor on the right.
//
// For the wordmark text we use system serif/sans fallbacks since
// resvg-js's font loading is OS-dependent and we want deterministic
// rendering. The disc uses inline glyph paths so it's pixel-identical
// to the on-page mark.
const svg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 480 120" width="480" height="120">
  <rect width="480" height="120" fill="#0b2545"/>

  <!-- Disc (light variant: navy-left blends with bg, white-right visible) -->
  <g transform="translate(20, 28) scale(1.0)">
    <defs>
      <clipPath id="disc-clip"><circle cx="32" cy="32" r="30"/></clipPath>
    </defs>
    <g clip-path="url(#disc-clip)">
      <rect x="0" y="0" width="32" height="64" fill="#0b2545"/>
      <rect x="32" y="0" width="32" height="64" fill="#ffffff"/>
    </g>
    <circle cx="32" cy="32" r="30" fill="none" stroke="#ffffff" stroke-width="2"/>
    <path fill="#ffffff" d="${PATH_3}"/>
    <path fill="#0b2545" d="${PATH_0}"/>
  </g>

  <!-- Wordmark: CIRP + descriptor, white on navy -->
  <text x="108" y="60" font-family="Georgia, 'Times New Roman', serif"
        font-weight="700" font-size="42" fill="#ffffff"
        dominant-baseline="alphabetic">CIRP</text>
  <text x="108" y="86" font-family="'Segoe UI', Helvetica, Arial, sans-serif"
        font-weight="500" font-size="12" fill="#c5d1e3"
        letter-spacing="2"
        dominant-baseline="alphabetic">CYBER INCIDENT RESPONSE PLANNER</text>
</svg>`;

const resvg = new Resvg(svg, {
  fitTo: { mode: 'width', value: 960 }, // 2× density for sharp rendering
  background: 'transparent',
});
const chipPng = resvg.render().asPng();
const chipBase64 = chipPng.toString('base64');

// --- Disc-only mark (the "30") for the Word page header ----------------
// Half-shaded disc (dark variant: white-left + navy-right) with the
// inline Fraunces glyphs. Rendered as a square for a top-left page-header
// mark — no surrounding chip, no wordmark.
const discSvg = `<?xml version="1.0" encoding="UTF-8"?>
<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 64 64" width="64" height="64">
  <defs><clipPath id="dc"><circle cx="32" cy="32" r="30"/></clipPath></defs>
  <g clip-path="url(#dc)">
    <rect x="0" y="0" width="32" height="64" fill="#ffffff"/>
    <rect x="32" y="0" width="32" height="64" fill="#0f1a2a"/>
    <line x1="32" y1="2" x2="32" y2="62" stroke="#ffffff" stroke-width="1.5" stroke-opacity="0.65"/>
  </g>
  <circle cx="32" cy="32" r="30" fill="none" stroke="#0f1a2a" stroke-width="1.5"/>
  <path fill="#0f1a2a" d="${PATH_3}"/>
  <path fill="#ffffff" d="${PATH_0}"/>
</svg>`;

const discResvg = new Resvg(discSvg, {
  fitTo: { mode: 'width', value: 192 }, // 3× density (target 64 px) for retina sharpness
  background: 'transparent',
});
const discPng = discResvg.render().asPng();
const discBase64 = discPng.toString('base64');

const out = `// Generated by scripts/generate-brand-image.mjs.
// Two PNGs, base64-inlined so both Node (examples script) and browser
// (docx export) can use them without filesystem access:
//
//   BRAND_CHIP_PNG_BASE64 — navy chip with disc + wordmark + descriptor
//                           (currently unused; kept for future use)
//   BRAND_DISC_PNG_BASE64 — bare half-shaded disc for the Word page header
//
// Do not edit by hand — re-run \`npm run generate:brand-image\` if either
// graphic changes.

/** Base64-encoded PNG of the navy brand chip (full lockup). */
export const BRAND_CHIP_PNG_BASE64 = ${JSON.stringify(chipBase64)};

/** Aspect ratio of the chip (width / height) — used to size the ImageRun. */
export const BRAND_CHIP_ASPECT = 4;

/** Base64-encoded PNG of the disc-only brand mark. */
export const BRAND_DISC_PNG_BASE64 = ${JSON.stringify(discBase64)};

function decodeBase64(b64: string): Uint8Array {
  const binary = atob(b64);
  const bytes = new Uint8Array(binary.length);
  for (let i = 0; i < binary.length; i++) bytes[i] = binary.charCodeAt(i);
  return bytes;
}

/** Raw PNG bytes for the navy chip. */
export function brandChipBytes(): Uint8Array {
  return decodeBase64(BRAND_CHIP_PNG_BASE64);
}

/** Raw PNG bytes for the disc-only mark. */
export function brandDiscBytes(): Uint8Array {
  return decodeBase64(BRAND_DISC_PNG_BASE64);
}
`;

writeFileSync(outPath, out);
console.log(`Wrote ${outPath}`);
console.log(`  chip PNG: ${chipPng.length} bytes, base64: ${chipBase64.length} chars`);
console.log(`  disc PNG: ${discPng.length} bytes, base64: ${discBase64.length} chars`);
