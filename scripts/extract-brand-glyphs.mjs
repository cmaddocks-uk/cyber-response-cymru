// One-off generator: extracts the "3" and "0" glyph outlines from Fraunces
// Bold (700) as SVG path data and writes them to src/components/brand-glyphs.ts.
//
// Re-run with `node scripts/extract-brand-glyphs.mjs` only if the brand
// numerals need to change (e.g. switching weights). The generated paths are
// committed so the runtime build does not depend on the font file or
// opentype.js at all.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import opentype from 'opentype.js';

const here = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(here, '..');
const fontPath = path.join(
  root,
  'node_modules',
  '@fontsource',
  'fraunces',
  'files',
  'fraunces-latin-700-normal.woff',
);
const outPath = path.join(root, 'src', 'components', 'brand-glyphs.ts');

const buf = readFileSync(fontPath);
const font = opentype.parse(
  buf.buffer.slice(buf.byteOffset, buf.byteOffset + buf.byteLength),
);

// Render each glyph at a size that fits a 64×64 viewBox comfortably. The
// resulting `path` has the glyph's baseline at y=0 and ascends upwards in
// font units, so we flip y and translate to land each digit inside the
// SVG's intended slot.
//
// Target placement:
//   "3" centred at (22, 32), "0" centred at (42, 32). Each digit is
//   ~32 units tall, baseline visually at ~y=44.
const FONT_SIZE = 36;        // glyph height in viewBox units
const BASELINE_Y = 42;       // visual baseline inside the 64-unit viewBox

function glyphPath(char, centerX) {
  const glyph = font.charToGlyph(char);
  // opentype.js .getPath(x, y, fontSize) places the baseline at y, so
  // we pass BASELINE_Y and let it produce final coordinates directly.
  // Then we centre horizontally by measuring advance width.
  const tmp = glyph.getPath(0, BASELINE_Y, FONT_SIZE);
  const bbox = tmp.getBoundingBox();
  const width = bbox.x2 - bbox.x1;
  const offsetX = centerX - (bbox.x1 + width / 2);
  return glyph.getPath(offsetX, BASELINE_Y, FONT_SIZE).toPathData(3);
}

// Centres tuned so each glyph fits cleanly inside its half of the 64-unit
// disc (left half ends at x=32). "3" gets the white half, "0" the navy.
const three = glyphPath('3', 20);
const zero = glyphPath('0', 44);

const out = `// Generated by scripts/extract-brand-glyphs.mjs from Fraunces 700.
// Do not edit by hand — re-run the script if the brand numerals change.

export const BRAND_3_PATH = ${JSON.stringify(three)};
export const BRAND_0_PATH = ${JSON.stringify(zero)};
`;

writeFileSync(outPath, out);
console.log(`Wrote ${outPath}`);
console.log(`  3: ${three.length} chars`);
console.log(`  0: ${zero.length} chars`);
